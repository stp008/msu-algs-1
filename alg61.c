#include <stdio.h>

int main() {
    int s;

    // Ввод значения s
    printf("Введите значение s (0 < s <= 18): ");
    if (scanf("%d", &s) != 1 || s <= 0 || s > 18) {
        printf("Ошибка ввода! s должно быть в диапазоне от 1 до 18.\n");
        return 1; // Завершаем программу при некорректном вводе
    }

    printf("Числа от 10 до 99, сумма цифр которых равна %d:\n", s);

    // Цикл с параметром: перебираем все числа от 10 до 99
    for (int number = 10; number <= 99; number++) {
        int digit1 = number / 10; // Первая цифра числа (десятки)
        int digit2 = number % 10; // Вторая цифра числа (единицы)

        // Проверка условия: сумма цифр равна s
        if (digit1 + digit2 == s) {
            printf("%d ", number); // Выводим число
        }
    }

    printf("\n");

    return 0;
}