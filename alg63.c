#include <stdio.h>

int main() {
    int a, b;

    // Ввод двух чисел
    printf("Введите два натуральных числа a и b (2 < a, b < 10000): ");
    if (scanf("%d %d", &a, &b) != 2 || a <= 2 || a >= 10000 || b <= 2 || b >= 10000) {
        printf("Ошибка ввода! Числа должны быть в диапазоне 2 < a, b < 10000.\n");
        return 1;
    }

    int temp_a = a, temp_b = b; // Создаем временные копии чисел для вычислений

    // Цикл с постусловием для вычисления НОД
    do {
        if (temp_a > temp_b) {
            temp_a = temp_a % temp_b; // Заменяем a на остаток от деления
        } else {
            temp_b = temp_b % temp_a; // Заменяем b на остаток от деления
        }
    } while (temp_a != 0 && temp_b != 0);

    // НОД - это ненулевое значение
    int gcd = (temp_a == 0) ? temp_b : temp_a;

    // Вывод результата
    printf("Наибольший общий делитель чисел %d и %d: %d\n", a, b, gcd);

    return 0;
}
