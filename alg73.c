#include <stdio.h>

int main() {
    int n;

    // Ввод числа n
    printf("Введите целое число n > 2: ");
    if (scanf("%d", &n) != 1 || n <= 2) {
        printf("Ошибка ввода! Число должно быть больше 2.\n");
        return -1;
    }

    printf("Простые числа из диапазона [2, %d]:\n", n);

    // Внешний цикл: перебор чисел от 2 до n
    for (int i = 2; i <= n; i++) {
        int is_prime = 1; // Флаг, является ли число простым

        // Внутренний цикл: проверка числа на простоту
        for (int j = 2; j * j <= i; j++) {
            if (i % j == 0) {
                is_prime = 0; // Число не является простым
                break;
            }
        }

        // Если число простое, выводим его
        if (is_prime) {
            printf("%d ", i);
        }
    }

    printf("\n");

    return 0;
}
