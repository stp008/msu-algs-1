#include <stdio.h>

int main() {
    int m;

    // Ввод значения m
    printf("Введите целое число m (m > 1): ");
    if (scanf("%d", &m) != 1 || m <= 1) {
        printf("Ошибка ввода! m должно быть целым числом больше 1.\n");
        return 1; // Завершаем программу при некорректном вводе
    }

    // Инициализация первых двух чисел Фибоначчи
    int f0 = 1;  // f(0)
    int f1 = 1;  // f(1)
    int fn = 0;  // Текущее число Фибоначчи

    // Используем цикл с предусловием для поиска числа Фибоначчи
    while (f1 <= m) {
        fn = f0 + f1;  // Вычисляем следующее число Фибоначчи
        f0 = f1;       // Обновляем предыдущее число
        f1 = fn;       // Обновляем текущее число
    }

    // Вывод результата
    printf("Первое число Фибоначчи, большее %d: %d\n", m, f1);

    return 0;
}
