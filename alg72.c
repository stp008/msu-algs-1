#include <stdio.h>

int main() {
    int n, skip; // Количество скобок и номер скобки для пропуска
    double product = 1.0; // Произведение всех сумм

    // Ввод количества скобок
    printf("Введите количество скобок n: ");
    if (scanf("%d", &n) != 1 || n <= 0) {
        printf("Ошибка ввода! n должно быть положительным числом.\n");
        return 1;
    }

    // Ввод номера скобки, которую нужно пропустить
    printf("Введите номер скобки, которую нужно пропустить (от 1 до %d): ", n);
    if (scanf("%d", &skip) != 1 || skip < 1 || skip > n) {
        printf("Ошибка ввода! Номер скобки должен быть от 1 до %d.\n", n);
        return 1;
    }

    // Внешний цикл: вычисление произведения
    for (int k = 1; k <= n; k++) {
        if (k == skip) {
            continue; // Пропускаем скобку с номером skip
        }

        double sum = 0.0; // Сумма для текущей скобки

        // Внутренний цикл: вычисление суммы 1/1 + 1/2 + ... + 1/m
        int m_start = (k - 1) * 20 + 1; // Начало диапазона для k-й скобки
        int m_end = k * 20;             // Конец диапазона для k-й скобки

        for (int i = m_start; i <= m_end; i++) {
            sum += 1.0 / i;
        }

        // Умножаем на текущую сумму
        product *= sum;
    }

    // Вывод результата
    printf("Результат произведения: %.10lf\n", product);

    return 0;
}
